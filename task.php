01. API üçün authentication tələb olunmur
02. UI (Frontend) tələb olunmur
03. Proyekt PHP ilə yazılmalıdır
04. PSR-1 və PSR-12 standartlarında yazılmalıdır (https://www.php-fig.org)
05. İstifadəçilərin siyahısını göstərmək və yeni istifadəçi yaratmaq üçün API yazılmalıdır
06. API üçün dokumentasiya yazılmalıdır
07. User modeli üçün id-dən başqa tələb yoxdur , istənilən sahə əlavə oluna bilər
08. Dependency injection istifadə edilməlidir (sadə variantda)
09. DI interfeyslərə qarşı kodlaşdırmalıdır və məlumat modelləri üçün Repository nümunələri tətbiq edilməlidir
10. Məlumat mənbəyindən istifadə edən və cavabı qaytaran koda toxunmadan users üçün məlumat mənbəyini dəyişdirə bilməkdir
11. Məlumat mənbəyinin dəyişdirilməsi sadəcə DB məlumatlarını dəyişdirmək deyil, MySQL NoSQL DB, CSV kimi fayllar tam məlumat mənbəyidir
12. Tətbiq müxtəlif məlumat mənbələrindən məlumatları emal edə bilməlidir
13. Məlumat mənbəyinin dəyişdirilməsi konfiqurasiya faylları vasitəsilə aparılmalıdır